$repo = new MenuRepository();
$allMenus = $repo->getAll();

// ครอบด้วย <pre> เพื่อให้รักษาระยะห่างของตัวอักษรให้เท่ากัน
echo "<pre style='font-family: monospace; font-size: 14px; line-height: 1.8;'>";

$count = 0;
foreach ($allMenus as $menu) {
    // ใช้ mb_str_pad หรือการคำนวณความยาวภาษาไทย เพื่อให้คอลัมน์ตรงกัน
    $itemString = sprintf("%2d. %s", $menu->id, $menu->name);
    
    // เติมช่องว่างด้านหลังเพื่อให้ความยาวตัวอักษรเท่ากัน
    $padLength = 30;
    $currentLen = mb_strlen($itemString, 'UTF-8');
    $spaces = str_repeat(' ', max(0, $padLength - $currentLen));
    
    echo $itemString . $spaces;
    
    $count++;
    
    // ครบ 5 เมนู ขึ้นบรรทัดใหม่ด้วย <br>
    if ($count % 5 === 0) {
        echo "<br>";
    }
}

echo "</pre>";